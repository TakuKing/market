package org.single.util;

import java.util.Date;
import java.util.Properties;
import java.util.UUID;

import javax.mail.Authenticator;
import javax.mail.Message;
import javax.mail.MessagingException;
import javax.mail.PasswordAuthentication;
import javax.mail.Session;
import javax.mail.internet.AddressException;
import javax.mail.internet.InternetAddress;
import javax.mail.internet.MimeMessage;

public class GmailVerify {
	
    public void sendMail(String mail, String uid) {
        
        Properties props = System.getProperties();
        props.put("mail.smtp.starttls.enable", "true");     // gmail은 무조건 true 고정
        props.put("mail.smtp.host", "smtp.gmail.com");      // smtp 서버 주소
        props.put("mail.smtp.auth", "true");                // gmail은 무조건 true 고정
        props.put("mail.smtp.port", "587");                 // gmail 포트
           
        Authenticator auth = new MyAuthentication();
         
        //session 생성 및  MimeMessage생성
        Session session = Session.getDefaultInstance(props, auth);
        MimeMessage msg = new MimeMessage(session);
         
        try{
            //편지보낸시간
            msg.setSentDate(new Date());
             
            InternetAddress from = new InternetAddress() ;
             
             
            from = new InternetAddress("kkangchang99@gmail.com");
             
            // 이메일 발신자
            msg.setFrom(from);
             
            System.out.println("가입메일 : " + mail);
            // 이메일 수신자  mail
            InternetAddress to = new InternetAddress(mail);
            msg.setRecipient(Message.RecipientType.TO, to);
             
            // 이메일 제목
            msg.setSubject("메일 전송 테스트 캬캬캬", "UTF-8");
             
            // 이메일 내용 
            // uuid
            msg.setText("<div>[내용] uuid : "+uid+"</div>"
            		  + "<div>링크 : <a href='http://localhost:9000/singleWeb/src/index.html?_ijt=aof8su2dqectdmf9o30tnv2g17'>uuid체크하러가기</a></div>", "UTF-8");
             
            // 이메일 헤더 
            msg.setHeader("content-Type", "text/html");
             
            //메일보내기
            javax.mail.Transport.send(msg);
             
        }catch (AddressException addr_e) {
            addr_e.printStackTrace();
        }catch (MessagingException msg_e) {
            msg_e.printStackTrace();
        }
    }
 
}
 
 
